package util;

import util.analisadorLexico.TokenType;

/**
 *
 * @author david
 */
public class Token {
    public TokenType type;
    public String value;

    public Token(TokenType type, String value) {
        this.type = type;
        this.value = value;
    }

    @Override
    public String toString() {
        return "[" + type + ": " + value + "]";
    }
}
